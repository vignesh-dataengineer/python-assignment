#String Slicing
s = input("Enter a word: ")

# 26. First 5 characters
print("First 5:", s[:5])

# 27. Last 3 characters
print("Last 3:", s[-3:])

# 28. Reverse string
print("Reverse:", s[::-1])

# 29. Every 2nd character
print("Every 2nd:", s[::2])

# 30. Remove first and last character
print("Without first & last:", s[1:-1])